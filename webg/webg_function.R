  a,
  b,
  c,
  d,
  e,
  f,
  g,
  h,
  i,
  j,
  k,
  l,
  m,
  n,
  o,
  p,
  q,
  r,
  s,
  t,
  u,
  v,
  w,
  x,
  y,
  z,
  aa,
  ab,
  ac,
  ad,
  ae,
  af,
  ag,
  ah,
  ai,
  aj
) {
  WebGestaltR(
    enrichMethod = a,
    organism = b,
    enrichDatabase = c,
    enrichDatabaseFile = d,
    enrichDatabaseType = e,
    enrichDatabaseDescriptionFile = f,
    interestGeneFile = g,
    interestGene = h,
    interestGeneType = i,
    collapseMethod = j,
    referenceGeneFile = k,
    referenceGene = l,
    referenceGeneType = m,
    referenceSet = n,
    minNum = o,
    maxNum = p,
    sigMethod = q,
    fdrMethod = r,
    fdrThr = s,
    topThr = t,
    reportNum = u,
    perNum = v,
    gseaP = w,
    isOutput = x,
    outputDirectory = y,
    projectName = z,
    dagColor = aa,
    saveRawGseaResult = ab,
    gseaPlotFormat = ac,
    setCoverNum = ad,
    networkConstructionMethod = ae,
    neighborNum = af,
    highlightType = ag,
    highlightSeedNum = ah,
    nThreads = ai,
    cache = aj,
    hostName = "http://www.webgestalt.org/"
  )
}